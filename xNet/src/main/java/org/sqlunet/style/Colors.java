	public static int[] getColors(@NonNull final Context context, @ColorRes final int... colorIds)
	{
		int[] result = new int[colorIds.length];
		for (int i = 0; i < colorIds.length; i++)
		{
			result[i] = context.getResources().getColor(colorIds[i]);
		}
		return result;
	}
